#include "ft_printf.h"

int	ft_numlen(long n)
{
	int	l;

	l = 0;
	if (n < 0)
	{
		l++;
		n *= -1;
	}
	if (n == 0)
		l++;
	while (n > 0)
	{
		l++;
		n /= 10;
	}
	return (l);
}

int	ft_hexlen(unsigned long n)
{
	int l;

	l = 0;
	if (n == 0)
		l++;
	while (n > 0)
	{
		l++;
		n /= 16;
	}
	return (l);
}
